package portfolio

import (
	"time"

	"github.com/penny-vault/pvbt/asset"
)

// PredictedHolding is a single position in a predicted portfolio, marked
// to the last available prices.
type PredictedHolding struct {
	Asset       asset.Asset
	Quantity    float64
	MarketValue float64
}

// Prediction records the outcome of a prediction run: the trades the
// strategy would place on the next scheduled trade date after the end of
// the run, and the holdings that would result. The engine attaches a
// Prediction to the account at the end of every backtest, and it is
// persisted to the SQLite output in dedicated tables.
type Prediction struct {
	// Date is the next scheduled trade date the prediction was run for.
	Date time.Time

	// Transactions are the trades generated by the prediction run, in the
	// order they were recorded.
	Transactions []Transaction

	// Holdings are the positions the portfolio would hold after the
	// predicted trades execute, sorted by ticker then FIGI.
	Holdings []PredictedHolding
}

// SetPrediction stores the outcome of a prediction run on the account.
// Passing nil clears any stored prediction.
func (a *Account) SetPrediction(pred *Prediction) {
	a.prediction = pred
}

// Prediction returns the stored prediction, or nil if no prediction run
// has been recorded.
func (a *Account) Prediction() *Prediction {
	return a.prediction
}
